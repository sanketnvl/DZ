package com.myexample.model;

public class JsonDashboardData {

	private int totalActiveUser;
	private int totalDocuments;
	private int totalUsers;
	private int totalSubscription;
	
	
	public int getTotalActiveUser() {
		return totalActiveUser;
	}
	public void setTotalActiveUser(int totalActiveUser) {
		this.totalActiveUser = totalActiveUser;
	}
	public int getTotalDocuments() {
		return totalDocuments;
	}
	public void setTotalDocuments(int totalDocuments) {
		this.totalDocuments = totalDocuments;
	}
	public int getTotalUsers() {
		return totalUsers;
	}
	public void setTotalUsers(int totalUsers) {
		this.totalUsers = totalUsers;
	}
	public int getTotalSubscription() {
		return totalSubscription;
	}
	public void setTotalSubscription(int totalSubscription) {
		this.totalSubscription = totalSubscription;
	}
	
	
}
